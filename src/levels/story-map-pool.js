export function getRandomMap(levelIndex) {
  const levelKey = `level${levelIndex + 1}`;
  const pool = MAP_POOLS[levelKey];

  if (!pool || pool.length === 0) {
    console.warn(`No maps for ${levelKey}`);
    return null;
  }

  const randomIndex = Math.floor(Math.random() * pool.length);
  return pool[randomIndex];
}

export function countSoulsInMap(map) {
  let count = 0;
  for (const row of map) {
    for (const char of row) {
      if (char === "S") count++;
    }
  }
  return count;
}
